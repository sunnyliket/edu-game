const WIDE_MAP_WIDTH = 128;
const WIDE_MAP_HEIGHT = 84;

function expandAdventureMap(baseMap, variant = 0) {
    const baseHeight = baseMap.length;
    const baseWidth = baseMap[0]?.length || 0;
    const rows = [];
    const isDesert = variant === 1;

    for (let y = 0; y < WIDE_MAP_HEIGHT; y += 1) {
        let row = "";
        for (let x = 0; x < WIDE_MAP_WIDTH; x += 1) {
            let tile = isDesert ? "S" : "G";
            const isOuterWall = x === 0 || y === 0 || x === WIDE_MAP_WIDTH - 1 || y === WIDE_MAP_HEIGHT - 1;

            if (isOuterWall) {
                tile = isDesert ? "K" : "T";
            } else if (y < baseHeight && x < baseWidth) {
                tile = baseMap[y][x] || "G";
                if (x === baseWidth - 1 && [2, 7, 10, 12].includes(y)) tile = "P";
                if (y === baseHeight - 1 && [5, 10, 15, 18].includes(x)) tile = "P";
                if (isDesert) {
                    if (tile === "G" || tile === "F") tile = "S";
                    if (tile === "T") tile = "K";
                }
            } else {
                const mainRoad = y === 7 || x === 10 || y === 18 || x === 28 || y === 36 || x === 54 || y === 52 || x === 78 || x === 104 || y === 68;
                const sideRoad = (x > 20 && y === 3) || (y > 13 && x === 18) || (x > 26 && y === 23) ||
                    (x > 44 && x < 118 && y === 12) || (y > 30 && y < 78 && x === 40) || (x > 58 && y === 45) ||
                    (x > 84 && y === 28) || (y > 52 && x === 112);

                if (isDesert) {
                    const oasis = ((x > 68 && x < 86 && y > 24 && y < 35) || (x > 98 && x < 119 && y > 54 && y < 65)) && (x + y) % 4 !== 0;
                    const canyon = (x > 31 && y > 3 && y < 11) || (x > 80 && y > 44 && y < 58) || (x > 104 && y > 18 && y < 34);
                    const dune = (x * 7 + y * 5 + variant) % 19 === 0;
                    const cactus = (x * 11 + y * 13 + variant) % 23 === 0;
                    const rock = (x * 3 + y * 11 + variant) % (canyon ? 10 : 31) === 0;
                    const ruin = ((x > 58 && x < 72 && y > 42 && y < 52) || (x > 96 && x < 113 && y > 66 && y < 78)) && (x + y + variant) % 4 === 0;

                    if (mainRoad || sideRoad) tile = "P";
                    else if (oasis) tile = "W";
                    else if (canyon || ruin) tile = (x + y + variant) % 3 === 0 ? "R" : "D";
                    else if (cactus) tile = "K";
                    else if (rock) tile = "R";
                    else if (dune) tile = "D";
                } else {
                    const river = ((x >= 24 && x <= 26) || (x >= 66 && x <= 69)) && ![7, 18, 23, 36, 45, 52].includes(y);
                    const lake = x > 70 && x < 86 && y > 24 && y < 34 && (x + y + variant) % 3 !== 0;
                    const cave = (x > 31 && y > 3 && y < 11) || (x > 80 && y > 44 && y < 58);
                    const grove = x > 44 && x < 70 && y > 4 && y < 22;
                    const deepForest = x > 18 && x < 46 && y > 38 && y < 60;
                    const forest = ((x * 5 + y * 7 + variant) % (grove || deepForest ? 7 : 13) === 0);
                    const rock = (x * 3 + y * 11 + variant) % (cave ? 11 : 29) === 0;
                    const flower = (x * 7 + y * 3 + variant) % 31 === 0;
                    const ruin = x > 58 && x < 72 && y > 42 && y < 52 && (x + y + variant) % 4 === 0;

                    if (mainRoad || sideRoad) tile = "P";
                    else if (river || lake) tile = "W";
                    else if (cave) tile = (x + y + variant) % 5 === 0 ? "R" : "D";
                    else if (ruin) tile = (x + y) % 2 === 0 ? "R" : "D";
                    else if (forest) tile = "T";
                    else if (rock) tile = "R";
                    else if (flower) tile = "F";
                }
            }

            row += tile;
        }
        rows.push(row);
    }

    return rows;
}

