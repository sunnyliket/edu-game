        this.addFacingDetail(faces, x, y + 0.76, z, 0.66, 0.54, 0, 0.78, 0.86, 0.16, 0.08, "#6d4420", face);
        this.addFacingDetail(faces, x, y + 1.08, z, 0.56, 0.56, -0.13, 1.39, 0.06, 0.07, 0.045, "#21170f", face);
