    startStageMusic() {
        const stage = this.getCurrentStage?.();
        const theme = this.getStageTheme?.();
        const music = theme?.music;
        if (!stage || !music) {
            this.stopStageMusic();
            return;
        }
        if (this.musicTimer && this.musicStageId === stage.id) return;
        this.stopStageMusic();

        try {
            const AudioContextClass = window.AudioContext || window.webkitAudioContext;
            if (!AudioContextClass) return;
            this.musicContext = this.musicContext || new AudioContextClass();
            this.musicContext.resume?.();
            this.musicStageId = stage.id;
            let index = 0;
            const playNote = () => {
                if (this.musicStageId !== stage.id) return;
                const now = this.musicContext.currentTime;
                const note = music.notes[index % music.notes.length];
                const bass = music.bass[Math.floor(index / 2) % music.bass.length];
                this.playStageTone(note, 0.52, music.wave, music.volume);
                if (index % 2 === 0) this.playStageTone(bass, 1.35, "sine", music.bassVolume);
                if (index % 4 === 0) {
                    this.playStageTone(bass * 1.5, 1.75, "triangle", music.bassVolume * 0.55);
                    this.playStageTone(bass * 2, 1.75, "triangle", music.bassVolume * 0.42);
                }
                index += 1;
            };
            playNote();
            this.musicTimer = setInterval(playNote, music.interval);
        } catch (error) {
            this.musicTimer = null;
            this.musicStageId = null;
        }
    },

    playStageTone(frequency, duration, type, volume) {
        if (!this.musicContext || !frequency) return;
        const now = this.musicContext.currentTime;
        const oscillator = this.musicContext.createOscillator();
        const gain = this.musicContext.createGain();
        const filter = this.musicContext.createBiquadFilter();
        oscillator.type = type;
        oscillator.frequency.setValueAtTime(frequency, now);
        filter.type = "lowpass";
        filter.frequency.setValueAtTime(type === "sawtooth" ? 520 : 1400, now);
        gain.gain.setValueAtTime(0.0001, now);
        gain.gain.exponentialRampToValueAtTime(Math.max(0.0002, volume), now + 0.04);
        gain.gain.exponentialRampToValueAtTime(0.0001, now + duration);
        oscillator.connect(filter);
        filter.connect(gain);
        gain.connect(this.musicContext.destination);
        oscillator.start(now);
        oscillator.stop(now + duration + 0.05);
    },

