function loadCharacterSprites() {
  if (typeof Image === "undefined") return;
  Object.values(CHARACTER_SPRITES).forEach((sprite) => {
    const img = new Image();
    img.onload = () => {
      sprite.loaded = true;
      render();
    };
    img.src = sprite.src;
    sprite.img = img;
  });
}

function drawSpriteCharacter(sprite, centerX, footY, height, label) {
  if (!sprite.loaded || !sprite.img) return false;
  const ratio = sprite.img.naturalWidth / sprite.img.naturalHeight;
  const w = Math.round(height * ratio);
  const h = Math.round(height);
  const x = Math.round(centerX - w / 2);
  const y = Math.round(footY - h);

  ch.imageSmoothingEnabled = false;
  pixelOval(ch, centerX, footY + 4, Math.round(w * 0.42), 8, "rgba(80, 65, 45, .22)", 2);
  ch.drawImage(sprite.img, x, y, w, h);

  const tagX = Math.round(centerX - (label.length > 2 ? 49 : 35));
  _nameTag(ch, tagX, y - 32, label);
  return true;
}

function charRect(ctx, x, y, w, h, color) {
  cpx(ctx, Math.round(x), Math.round(y), color, Math.max(1, Math.round(w)), Math.max(1, Math.round(h)));
}

function pixelOval(ctx, cx, cy, rx, ry, color, row = 2) {
  for (let y = -ry; y <= ry; y += row) {
    const half = Math.max(1, Math.round(rx * Math.sqrt(Math.max(0, 1 - (y * y) / (ry * ry)))));
    charRect(ctx, cx - half, cy + y, half * 2, row, color);
  }
}

