/** 픽셀 단위로 사각형 채우기 */
function px(ctx, x, y, color, w = 1, h = 1) {
  if (!color) return;
  ctx.fillStyle = color;
  ctx.fillRect(x * PX, y * PX, w * PX, h * PX);
}

/** 캐릭터 전용 작은 픽셀 단위로 사각형 채우기 */
function cpx(ctx, x, y, color, w = 1, h = 1) {
  if (!color) return;
  ctx.fillStyle = color;
  ctx.fillRect(x * CPX, y * CPX, w * CPX, h * CPX);
}

